  <h1>Bienvenue sur le site de gestion des entretiens d'entrée à Polytech Annecy-Chambéry</h1>
  <p> Ce site est pour réaliser le projet de INFO642-Base de données et technologies web.Chaque année, à diverses occasions, des entretiens sont organisés au sein de Polytech Annecy-Chambéry :
  </p>
  <ul style="font-size:18px;">
  <li>Les entretiens de recrutements (PEIP ou filière ingénieurs)</li>
  <li>Les évaluations de compétences des étudiants dans le cadre des APP</li>
  <li>Les évaluation de projets (projet usage, recherche & développement, ...)</li>
  <li>Les évaluation de stages (stage ingénieur, ...)</li>
  </ul>  
  <p>Ces sessions d'entretiens mobilisent à chaque fois plusieurs ressources :</p>
  <ul style="font-size:18px;">
  <li>des étudiants ou futurs étudiants</li>
  <li>des enseignants</li>
  <li>des salles</li>
  </ul> 
<p>Une session est généralement organisée sur plusieurs demi-journées. Les membres du jury sont bloqués sur des demi-journées entières selon leurs disponibilités. Le jury se compose d'au moins 2 enseignants. Les étudiants eux sont convoqués sur une plage horaire précise.</p>  
<p>Après chaque entretien, chaque membre du jury réalise individuellement une évaluation qu'il saisit dans une grille composée de critères. L'expression des critères ainsi que  leur niveau d’évaluation peut différer selon les types de session.</p><br>  
